package com.example.studentManagement;

public class MarksForm {

    private int sid;
    private int teluguMarks;
    private int hindiMarks;
    private int englishMarks;
    private int mathsMarks;


    public int getSid() {return sid;}

    public void setSid(int sid) {this.sid = sid;}

    public int getTeluguMarks() {return teluguMarks;}

    public void setTeluguMarks(int teluguMarks) {this.teluguMarks = teluguMarks;}

    public int getHindiMarks() {return hindiMarks;}

    public void setHindiMarks(int hindiMarks) {this.hindiMarks = hindiMarks;}

    public int getEnglishMarks() {return englishMarks;}

    public void setEnglishMarks(int englishMarks) {this.englishMarks = englishMarks;}

    public int getMathsMarks() {return mathsMarks;}

    public void setMathsMarks(int mathsMarks) {this.mathsMarks = mathsMarks;}
}
